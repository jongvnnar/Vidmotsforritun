    private void lessThanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lessThanButtonActionPerformed
        // TODO add your handling code here:
        try{
            int x = Integer.parseInt(giskText.getText());
            if(x<y){
                showLessThanLabel.setText("x < y");
            }
            else{
                showLessThanLabel.setText("x >= y");
            }
            cnt++;
            if(cnt >= 8){
                lessThanButton.setEnabled(false);
                giskText.setEnabled(false);
            }
            numOfTriesLabel.setText(Integer.toString(cnt));
        }
        catch(Exception e){
        }
    }//GEN-LAST:event_lessThanButtonActionPerformed

